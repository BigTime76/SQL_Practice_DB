BEGIN TRAN

	CREATE TABLE pubCo (
	publisherName varchar(64) PRIMARY KEY,
	publisherWww varchar(256) NULL,
	publisherEmail varchar(64) NULL
	)

	CREATE TABLE book (
	bookId int PRIMARY KEY,
	bookTitle varchar(128) NULL,
	publisherName varchar(64),
	)

	CREATE TABLE bookAuthors (
	bookId int,
	authorName varchar(128)
	)

	CREATE TABLE libraryBranch (
	branchId int PRIMARY KEY,
	branchName varchar(64),
	branchAddress varchar(256)
	)

	CREATE TABLE bookCopies (
	bookId int FOREIGN KEY REFERENCES book(bookId),
	branchId int FOREIGN KEY REFERENCES libraryBranch(branchId),
	numOfCopies int,
	)

	CREATE TABLE borrower (
	libCardNo int PRIMARY KEY,
	memberName varchar(64),
	memberAddress varchar(256),
	memberPhone varchar(32)
	)

	CREATE TABLE bookLoans (
	bookId int FOREIGN KEY REFERENCES book(bookId),
	branchId int FOREIGN KEY REFERENCES libraryBranch(branchId),
	libCardNo int FOREIGN KEY REFERENCES borrower(libCardNo),
	dateOut date,
	dateDue date
	)

